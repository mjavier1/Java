package udemy.poo.codigo;

/**
 *
 * @author mj
 */
public class componetes {
    private float tiempo;
    private float x;
    private float y;

    public componetes(float tiempo, float x, float y) {
        this.tiempo = tiempo;
        this.x = x;
        this.y = y;
    }

    public float getTiempo() {
        return tiempo;
    }

    public void setTiempo(float tiempo) {
        this.tiempo = tiempo;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
    
    
    
}
